    xmlns:tools="http://schemas.android.com/tools">
    <!-- GENERATED by endevir - DO NOT EDIT
         古いandroidx.test:core（Flutter embeddingの1.2.0系制約に合わせた
         バージョン）のマニフェストがtargetSdk 31+のexported明示要件を
         満たさないため、ここで上書きする（ADR-006の落とし穴3） -->
    <application>
        <activity
            android:name="androidx.test.core.app.InstrumentationActivityInvoker\$BootstrapActivity"
            android:exported="false"
            tools:node="merge" />
        <activity
            android:name="androidx.test.core.app.InstrumentationActivityInvoker\$EmptyActivity"
            android:exported="false"
            tools:node="merge" />
        <activity
            android:name="androidx.test.core.app.InstrumentationActivityInvoker\$EmptyFloatingActivity"
            android:exported="false"
            tools:node="merge" />
    </application>
</manifest>
''';

Future<int> runNativeCommand(List<String> args) async {
  final parser = ArgParser()
    ..addFlag('run', help: 'connectedAndroidTestを実行する')
    ..addFlag('build',
        help: 'デバイスファーム持ち込み用APK（app + androidTest）をビルドする')
    ..addOption('device', abbr: 'd', help: 'adbシリアル（--run時）')
    ..addOption('target',
        abbr: 't', defaultsTo: 'endevir_test/main_test.dart')
    ..addFlag('help', abbr: 'h', negatable: false);
  final options = parser.parse(args);
  if (options['help'] as bool || args.isEmpty || args.first != 'android') {
    print('usage: endevir native android [--run [-d serial]] [-t target]');
    print(parser.usage);
    return args.isEmpty ? 64 : 0;
  }
  final rest = parser.parse(args.skip(1).toList());

  scaffoldAndroidNative(Directory.current.path);
  print('[endevir] android instrumentation scaffold updated');

  if (rest['build'] as bool) {
    return _buildFarmApks(rest['target'] as String);
  }

  if (!(rest['run'] as bool)) {
    print('');
    print('[endevir] 次のステップ:');
    print('  ローカル実行:              endevir native android --run');
    print('  デバイスファーム用APK作成: endevir native android --build');
    return 0;
  }

  // gradle実行用のJDKを解決する（ADR-006の落とし穴1）
  final environment = Map<String, String>.from(Platform.environment);
  final jdk = await _resolveJdk();
  if (jdk != null) {
    environment['JAVA_HOME'] = jdk;
    print('[endevir] JAVA_HOME: $jdk');
  }

  print('[endevir] gradlew connectedDebugAndroidTest');
  final process = await Process.start(
    './gradlew',
    [
      ':app:connectedDebugAndroidTest',
      '-Ptarget=${rest['target'] as String}',
    ],
    workingDirectory: 'android',
    environment: environment,
    mode: ProcessStartMode.inheritStdio,
  );
  return process.exitCode;
}

/// デバイスファーム（Firebase Test Lab等）持ち込み用のAPKをビルドする。
/// app APKは必ずテストエントリポイント（エージェント入り）でビルドする——
/// 素のlib/main.dartビルドを持ち込むと全テストがエージェント接続待ちで失敗する。
Future<int> _buildFarmApks(String target) async {
  print('[endevir] build app APK (target: $target)');
  final flutterProcess = await Process.start(
    flutterExecutable(),
    [...flutterArgPrefix(), 'build', 'apk', '--debug', '-t', target],
    mode: ProcessStartMode.inheritStdio,
  );
  if (await flutterProcess.exitCode != 0) return 1;

  final environment = Map<String, String>.from(Platform.environment);
  final jdk = await _resolveJdk();
  if (jdk != null) environment['JAVA_HOME'] = jdk;

  print('[endevir] build androidTest APK');
  final gradleProcess = await Process.start(
    './gradlew',
    [':app:assembleDebugAndroidTest', '-Ptarget=$target'],
    workingDirectory: 'android',
    environment: environment,
    mode: ProcessStartMode.inheritStdio,
  );
  if (await gradleProcess.exitCode != 0) return 1;

  const appApk = 'build/app/outputs/flutter-apk/app-debug.apk';
  const testApk =
      'build/app/outputs/apk/androidTest/debug/app-debug-androidTest.apk';
  print('');
  print('[endevir] APKを生成しました:');
  print('  app:  $appApk');
  print('  test: $testApk');
  print('');
  print('[endevir] Firebase Test Labで実行するには:');
  print('  gcloud firebase test android run \\');
  print('    --type instrumentation \\');
  print('    --app $appApk \\');
  print('    --test $testApk \\');
  print('    --device model=MediumPhone.arm,version=33');
  return 0;
}

/// Gradle/Kotlinと互換のあるJDK（17〜21）を探す。
Future<String?> _resolveJdk() async {
  // 1) 現在のJAVA_HOMEが互換ならそのまま
  final current = Platform.environment['JAVA_HOME'];
  if (current != null && await _isCompatibleJdk('$current/bin/java')) {
    return current;
  }
  // 2) Flutterが設定するjdk-dir
  final config = await Process.run(
      flutterExecutable(), [...flutterArgPrefix(), 'config', '--list']);
  final jdkDir = RegExp(r'jdk-dir:\s*(.+)')
      .firstMatch(config.stdout as String)
      ?.group(1)
      ?.trim();
  if (jdkDir != null &&
      jdkDir != '(Not set)' &&
      await _isCompatibleJdk('$jdkDir/bin/java')) {
    return jdkDir;
  }
  // 3) macOS: java_homeで17を探す
  if (Platform.isMacOS) {
    final result =
        await Process.run('/usr/libexec/java_home', ['-v', '17']);
    if (result.exitCode == 0) {
      return (result.stdout as String).trim();
    }
  }
  print('[endevir] WARNING: 互換JDK（17〜21）が見つかりません。'
      'gradleが失敗する場合はJAVA_HOMEを設定してください');
  return null;
}

Future<bool> _isCompatibleJdk(String javaPath) async {
  try {
    final result = await Process.run(javaPath, ['-version']);
    final major = int.tryParse(
        RegExp(r'version "(\d+)').firstMatch('${result.stderr}')?.group(1) ??
            '');
    return major != null && major >= 17 && major <= 21;
  } on ProcessException {
    return false;
  }
}
